'''

If a runner runs 10 miles in 30 minutes and 30 seconds,
What is his/her average speed in kilometers per hour? (Tip: 1 mile = 1.6 km)

'''
# distance in km
d = 10 * 1.6
# Traveling time in hour
t = 30.5 / 60
# speed = distance / time
s = d / t

print(f"The runner's average speed is {s} kilometers per hour.")
